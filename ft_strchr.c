#include <stdio.h>

char	*ft_strchr(char *s, int c)
{
	int	i;
   	char	*match;

    i = 0;
    while (s[i] != '\0')
    {
        if (s[i] == c)
        {
            match = &s[i];
            return (match);
        }
        i++;
    }
}
